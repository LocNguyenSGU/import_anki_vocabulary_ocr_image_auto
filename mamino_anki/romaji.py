def _katakana_to_hiragana(text: str) -> str:
    chars = []
    for char in text:
        code = ord(char)
        if 0x30A1 <= code <= 0x30F6:
            chars.append(chr(code - 0x60))
        else:
            chars.append(char)
    return "".join(chars)


def _double_consonant(next_romaji: str) -> str:
    if next_romaji.startswith("ch"):
        return "t"
    return next_romaji[0] if next_romaji else ""


def kana_to_romaji(kana: str) -> str:
    text = _katakana_to_hiragana(kana)
    result: list[str] = []
    i = 0
    while i < len(text):
        char = text[i]
        pair = text[i : i + 2]

        if char == "ー":
            previous = result[-1] if result else ""
            result.append(previous[-1] if previous and previous[-1] in "aeiou" else "")
            i += 1
            continue

        if char == "っ":
            next_pair = text[i + 1 : i + 3]
            next_char = text[i + 1 : i + 2]
            next_romaji = DIGRAPHS.get(next_pair) or BASE.get(next_char, "")
            result.append(_double_consonant(next_romaji))
            i += 1
            continue

        if pair in DIGRAPHS:
            result.append(DIGRAPHS[pair])
            i += 2
            continue

        if char in BASE:
            result.append(BASE[char])
            i += 1
            continue

        result.append(char)
        i += 1

    return "".join(result)
